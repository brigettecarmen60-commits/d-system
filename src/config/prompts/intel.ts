export function buildIntelUserMessage(input: { niche: string; searchResults: Record<string, string> }): string {
  const dimensionLabels: Record<string, string> = {
    money: "钱——市场规模/客单价/毛利率/盈利模式",
    pit: "坑——行业乱象/失败原因/用户被骗/潜规则",
    people: "人——用户画像/痛点/购买动机",
    landscape: "局——竞争格局/头部玩家/新进入者机会",
    traffic: "流——流量来源/获客方式/获客成本",
    verdict: "判——行业前景/投资机会/风险/趋势",
  }

  // Build with global sequential numbering across all dimensions
  let searchBlock = ""
  let globalIndex = 1
  for (const [dim, label] of Object.entries(dimensionLabels)) {
    const content = input.searchResults[dim]
    if (content && content.trim()) {
      // Renumber per-result [N] markers to global [搜N]
      const renumbered = content.trim().replace(/^\[(\d+)\]\s/gm, () => `[搜${globalIndex++}] `)
      searchBlock += `\n### ${label}\n${renumbered}\n`
    } else {
      searchBlock += `\n### ${label}\n（搜索未返回有效结果）\n`
    }
  }

  return [
    `## 赛道：${input.niche}`,
    "",
    "## 搜索结果（唯一数据来源——只能引用以下内容，禁止引用区块外的任何数据）",
    searchBlock,
    "",
    "请按六点雷达扫描框架输出分析。记住：",
    "1. 每个事实性声明必须标注 [搜X] 来源编号",
    "2. 搜索结果没有的，标注「⚠️ 数据不足」",
    "3. 不许编造任何报告名、媒体名、案例细节",
  ].join("\n")
}
