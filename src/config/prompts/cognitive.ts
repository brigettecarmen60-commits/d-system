export function buildCognitiveUserMessage(input: { niche: string; targetGap?: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.targetGap) parts.push("【目标受众】" + input.targetGap)
  if (input.dna) { parts.push(""); parts.push("【DNA / 定位信息】"); parts.push(input.dna) }
  parts.push("")
  parts.push("脑内完整执行：17母题全量扫描→13跨域方向判断→赛道温度判断→大流量缺口三问→立场锁定→9步出题。但只输出4条精简选题。每条含：标题+拍摄形式+为什么（一句话人性共识）。")
  return parts.join("\n")
}
