export function buildSeriesUserMessage(input: {
  niche: string
  edge?: string
  dna?: string
}): string {
  const parts: string[] = []
  parts.push("【赛道】" + input.niche)

  if (input.edge && input.edge.trim()) {
    parts.push("")
    parts.push("【底牌】" + input.edge)
  } else {
    parts.push("")
    parts.push("【底牌】用户没给个人经历。不要编造——也不要给「观众想避坑」「展示专业知识」这种正确的废话。")
    parts.push("")
    parts.push("做这件事：找出这个赛道背后最核心的人性冲突。")
    parts.push("二手车背后是信任（我被骗了吗），家电清洗背后是恐惧（我家的空气在害我孩子吗），陪诊背后是孤独（我在这个城市里一个人看病）。找到那个冲突，系列方向从这个冲突里长出来。")
    parts.push("")
    parts.push("然后给出3个系列方向。每个方向必须具体到：片名、第一集拍什么（具体的画面，不是标题）、观众为什么会在0.5秒内停下来。不要说「观众想学XX」——说观众在深夜刷手机时在害怕什么/好奇什么/被什么困扰。")
  }

  if (input.dna) {
    parts.push("")
    parts.push("【DNA/定位】")
    parts.push(input.dna)
  }

  parts.push("")
  parts.push("做系列策划。主语是观众。用自然段落写，不要模板格式。说清楚观众为什么看、为什么只有你能做、拍什么具体内容。")
  return parts.join("\n")
}
