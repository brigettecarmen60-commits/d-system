export function buildTrustTopicUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push("【DNA / 定位信息】"); parts.push(input.dna) }
  parts.push("")
  parts.push("脑内完整执行：信任证据链诊断(六种最缺哪2-3种)→讨喜共识锁定(4个)→造神主线+信任标签→发愿状态检查→形式路由判断。但只输出4条精简选题。每条含：标题+拍摄形式+为什么。证据姿态：事件流露>他人证言>配角行为>自己说，自己说≤1条。")
  return parts.join("\n")
}
