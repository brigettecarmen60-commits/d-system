export function buildModeBUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push("【DNA/定位】"); parts.push(input.dna) }
  return parts.join("\n")
}

export function buildModeBAnalysisUserMessage(input: { niche: string }): string {
  return "【赛道】" + input.niche + "\n\n只输出JSON。不做选题。"
}

export function buildModeBGenUserMessage(input: { niche: string; analysis: string }): string {
  return "【赛道】" + input.niche + "\n\n【赛道分析】\n" + input.analysis + "\n\n基于以上分析，生成4条荒诞选题。每条必须基于分析里的一个物理原子+一个桥段。"
}
