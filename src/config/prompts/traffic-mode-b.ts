export function buildModeBUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push(input.dna) }
  parts.push("")
  parts.push("像上面10条例子那样，给这个赛道出4条荒诞选题。每条有一个具体规则逼真实的人做选择。")
  return parts.join("\n")
}
