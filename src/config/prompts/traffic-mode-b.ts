export function buildModeBUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push("【DNA / 定位信息】"); parts.push(input.dna) }
  parts.push("")
  parts.push("脑内完整执行三维错位老虎机（拆解枯燥切片→抽取荒诞模具→匹配情绪代码→设计视觉奇观），但只输出4条精简选题。每条含：标题+拍摄形式+为什么。")
  return parts.join("\n")
}
