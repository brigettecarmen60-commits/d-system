export function buildModeBUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push("【DNA/定位】"); parts.push(input.dna) }
  parts.push("")
  parts.push("先列物理东西→从桥段库选一个编号→把物理东西塞进桥段→出题。把好的荒诞长什么样作为参考，但用你自己的赛道。")
  return parts.join("\n")
}
