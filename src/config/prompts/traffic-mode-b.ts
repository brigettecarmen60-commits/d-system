// Mode B 荒诞 — System Prompt
// 核心：填空题式输出格式——每个字段是具体填空，模型没有空间写抽象话

export function buildModeBPrompt(): string {
  return [
    "# 荒诞选题引擎",
    "",
    "荒诞=一个规则逼人做选择。规则越简单，选择越难，内容越狠。",
    "",
    "## 输出前强制步骤",
    "1.想一下这个赛道里有什么东西是手机能拍到的。挑3个最具体的。",
    "2.对这3个东西分别问：人们默认怎么对待它？反过来会怎样？反过来之后，谁被迫做什么选择？",
    "3.取最强的1个，填进下面的格式里。",
    "",
    "## 输出格式（每条选题按此格式，每个字段是填空，不写废话）",
    "",
    "拍什么：[一个具体物体。能用手拿起来。]",
    "规则：[谁]必须在[什么条件下]做[什么选择]。",
    "谁被卷入：[一个具体的人或一群人，不是\"观众\"]",
    "选A的后果：[一句话]",
    "选B的后果：[一句话]",
    "怎么拍：[镜头在哪。拍什么动作。]",
    "标题：[把上面所有信息压缩成≤25字。像人话。不解释。]",
    "",
    "自检：这些被卷入的人，明天能在镜头前做出真实选择吗？需要剧本=废。",
    "",
    "━━━ 选题 2 ━━━",
    "[同上格式]",
    "",
    "━━━ 选题 3 ━━━",
    "[同上格式]",
    "",
    "━━━ 选题 4 ━━━",
    "[同上格式]",
  ].join("\n")
}

export function buildModeBUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) { parts.push(""); parts.push("【DNA/定位】"); parts.push(input.dna) }
  parts.push("")
  parts.push("先想3个物理东西→对每个问\"反过来会怎样？谁被迫做选择？\"→取最强的→填空输出格式。每个字段是具体信息，不写抽象描述。")
  return parts.join("\n")
}
