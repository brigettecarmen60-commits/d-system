export function buildSeedingGenUserMessage(input: {
  product: string
  routing: string
  audience?: string
  embedDepth?: string
  storytellerCustom?: string
  medium?: string
  depth?: string
  structure?: string
}): string {
  const parts = [`【产品】${input.product}`]
  parts.push(""); parts.push("【路由决策】"); parts.push(input.routing)
  if (input.audience) { parts.push(""); parts.push(`【目标人群】${input.audience}`) }
  if (input.embedDepth && input.embedDepth !== "narrative-carrier") {
    parts.push(""); parts.push(`【嵌入深度约束】${input.embedDepth}`)
  }
  if (input.storytellerCustom) {
    parts.push(""); parts.push(`【自定义讲故事的人】${input.storytellerCustom}`)
  }
  if (input.medium && input.medium !== "auto") {
    parts.push(""); parts.push(`【介质覆盖】用户指定了${input.medium}——忽略路由决策中的介质，用这个。`)
  }
  if (input.depth && input.depth !== "standard") {
    parts.push(""); parts.push(`【叙事深度覆盖】用户指定了${input.depth}级深度。路由决策中的技法数量可能不符合这个深度，你需要调整：short只用1个技法，standard用2个，long用3个。`)
  }
  if (input.structure && input.structure !== "auto") {
    parts.push(""); parts.push(`【脚本结构覆盖】用户指定了${input.structure}——忽略路由决策中的结构，用这个。`)
  }
  parts.push(""); parts.push("按照路由决策写脚本。只输出脚本，不输出任何诊断信息。")
  return parts.join("\n")
}

