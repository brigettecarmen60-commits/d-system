export function buildSeedingRoutingUserMessage(input: {
  product: string
  face?: string; vow?: string; audience?: string
  framework?: string; storyteller?: string; storytellerCustom?: string
  medium?: string
}): string {
  const parts = [`【产品】${input.product}`]
  if (input.face) { parts.push(""); parts.push(`【人设脸谱】${input.face}`) }
  if (input.vow) { parts.push(""); parts.push(`【发愿】${input.vow}`) }
  if (input.audience) { parts.push(""); parts.push(`【目标人群】${input.audience}`) }
  if (input.framework && input.framework !== "auto") {
    parts.push(""); parts.push(`【用户指定框架】${input.framework}——用这个框架，技法从对应的技法库里选。`)
  }
  if (input.storyteller && input.storyteller !== "auto") {
    parts.push(""); parts.push(`【用户指定讲故事的人】${input.storyteller}`)
  }
  if (input.storytellerCustom) {
    parts.push(""); parts.push(`【自定义讲故事的人】${input.storytellerCustom}`)
  }
  if (input.medium && input.medium !== "auto") {
    parts.push(""); parts.push(`【用户指定介质】${input.medium}——必须用这个介质`)
  }
  parts.push(""); parts.push("只输出JSON。不写故事。")
  return parts.join("\n")
}

