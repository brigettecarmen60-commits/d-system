export function buildSprintUserMessage(input: { stage: string; goal: string; niche?: string; recentData?: string }): string {
  const { stage, goal, niche, recentData } = input
  const stageLabels: Record<string, string> = {
    A: "从零开始（0内容或0粉丝）——什么都没验证，一切是假设",
    B: "有内容但没方向（数据无规律）——缺乏系统性",
    C: "有方向但没规模（知道什么有效但不稳定）——需要复制放大",
    D: "有规模但没转化（有流量不赚钱）——收割链路没通",
    E: "转型/重启（老号换赛道或长期停更）——重新校准",
  }

  const parts = [
    `【账号阶段】${stageLabels[stage] || stage}`,
    `【季度目标】${goal}`,
  ]
  if (niche) parts.push(`【赛道】${niche}`)
  if (recentData) {
    parts.push("")
    parts.push("【近期数据】")
    parts.push(recentData)
  }
  parts.push("")
  parts.push("请按 Part 0→1→2→3→4 完整输出运营规划。每Part标注🛑 STOP POINT。核心哲学：不预测未来，设计学习速度。不做90天日历。")
  return parts.join("\n")
}
