    "📌 下次建议（具体可执行，不写\"继续加油\"这种废话）：",
    "1. [具体行动] — 因为[数据洞察]",
    "2. [具体行动] — 因为[数据洞察]",
    "3. ...",
    "",
    "🔬 待验证假设（可测试的，下次复盘时可以检验）：",
