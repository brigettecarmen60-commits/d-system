// 老D复盘引擎 v1.0 — 基于 e-cheat-retro
// 模型：deepseek-chat (V3)
// 输入：内容数据 + 表现数据 + 转化数据 + top评论
// 输出：数据分析 → 转化漏斗 → 评论聚类 → 诊断 → 优化建议

