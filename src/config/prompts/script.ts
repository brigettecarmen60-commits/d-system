export function buildScriptUserMessage(input: { topic: string; contentType?: string; dna?: string }): string {
  const parts = ["【选题】" + input.topic]
  if (input.dna) {
    parts.push("")
    parts.push("【传递卡 / DNA】")
    parts.push(input.dna)
    parts.push("（读出镜人的人设、发愿、立场、语调、三个钉子——这些决定脚本的语气和关系。没有传递卡=用通用语气。）")
  }
  if (input.contentType && input.contentType !== "auto") {
    parts.push("【内容目的偏好】" + input.contentType)
  }
  parts.push("")
  parts.push("先做类型判定：这条选题是框架驱动（Vlog/记录/挑战/对谈/POV/身份倒置…）还是文案驱动（口播/旁白/演绎…）？框架驱动→输出拍摄框架（亮点节点+捕捉信号+拍摄指令）。文案驱动→执行完整管线：读传递卡→to who→叙事结构→情绪设计→开头→中段→结尾→去AI味→校验→输出逐字稿。如果有传递卡，整条脚本的语气/关系/钉子必须匹配传递卡。转化型中段必须包含≥2个具体数字+≥1个案例+1个诚实时刻。")
  return parts.join("\n")
}
