export function buildRetellUserMessage(input: {
  material: string
  framework?: string
  medium?: string
  emotion?: string
  structure?: string
}): string {
  const frameworkNames: Record<string, string> = {
    "time-entropy": "时间熵增与不可逆",
    "unequal-exchange": "关系中的非等价交换",
    "mirror-reconciliation": "镜像与和解",
    "giant-mayfly": "巨物与蜉蝣",
    "ordinary-journey": "凡人之旅",
    "hidden-order": "隐秘的秩序",
  }

  const parts = ["【真实事件素材】" + input.material]

  if (input.framework && input.framework !== "auto") {
    parts.push("")
    parts.push(`【指定框架】${frameworkNames[input.framework] || input.framework}`)
    parts.push("使用这个框架的技法库。从技法库中选2-3个最匹配素材的技法，落实到脚本的镜头和台词中。")
  } else {
    parts.push("")
    parts.push("【框架】自动判定——读素材，判断它天然适配哪个框架，然后使用该框架的技法库。")
  }

  if (input.structure && input.structure !== "auto") {
    parts.push("")
    parts.push(`【指定结构】${input.structure}`)
  }

  if (input.emotion && input.emotion !== "auto") {
    parts.push("")
    parts.push(`【情绪落点】观众看完应该感受到：${input.emotion}`)
  }

  if (input.medium && input.medium !== "auto") {
    parts.push(`【介质】${input.medium}`)
  }

  parts.push("")
  parts.push("只输出脚本正文。不输出框架名、技法名、结构名。直接开始。")

  return parts.join("\n")
}
