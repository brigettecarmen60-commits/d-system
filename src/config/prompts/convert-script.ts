export function buildConvertScriptRoutingUserMessage(input: { topic: string }): string {
  return "【选题】" + input.topic + "\n\n只输出JSON。不写脚本。"
}

export function buildConvertScriptGenUserMessage(input: {
  topic: string; routing: string
  painPoint?: string; solution?: string
  showMuscle?: string; evidencePosture?: string
  dna?: string; ctaPreference?: string
}): string {
  const parts = ["【选题】" + input.topic]
  parts.push("")
  parts.push("【路由决策】" + input.routing)
  if (input.painPoint) { parts.push(""); parts.push("【用户痛点】"); parts.push(input.painPoint) }
  if (input.solution) { parts.push(""); parts.push("【解决方案】"); parts.push(input.solution) }
  if (input.showMuscle && input.showMuscle !== "auto") parts.push("【展示方式】" + input.showMuscle)
  if (input.evidencePosture && input.evidencePosture !== "auto") parts.push("【证据姿态偏好】" + input.evidencePosture)
  if (input.dna) { parts.push(""); parts.push("【DNA/传递卡】"); parts.push(input.dna) }
  if (input.ctaPreference && input.ctaPreference !== "auto") parts.push("【CTA偏好】" + input.ctaPreference)
  parts.push("")
  parts.push("写转化脚本。关系=踩过坑的人在交底。每信息点绑定证据。诚实时刻在70%。闭环+决策节点匹配CTA。过L1-L3自检。")
  return parts.join("\n")
}
