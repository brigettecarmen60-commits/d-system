export function buildStoryRoutingUserMessage(input: { material: string; dna?: string }): string {
  const parts = ["【真实故事素材】" + input.material]
  if (input.dna) { parts.push(""); parts.push("【传递卡/DNA】"); parts.push(input.dna) }
  parts.push(""); parts.push("只输出JSON框架决策。不写故事。")
  return parts.join("\n")
}

export function buildStoryGenUserMessage(input: { material: string; routing: string; dna?: string; medium?: string }): string {
  const parts = ["【真实故事素材】" + input.material]
  parts.push(""); parts.push("【框架决策】"); parts.push(input.routing)
  if (input.dna) { parts.push(""); parts.push("【传递卡/DNA】"); parts.push(input.dna) }
  if (input.medium && input.medium !== "auto") parts.push("【介质】" + input.medium)
  parts.push("")
  parts.push("根据框架决策写人设故事脚本。开头激活指定的认知异常类型。介质决定写法——口播用身体感写法，Vlog用白描旁白。遵守编导三问。结尾不给答案。命中造神模板和讨喜共识。写完AI腔扫描。只输出脚本正文（🎙️/🎥+🎙️），不输出诊断卡/框架名/技法名/校验。")
  return parts.join("\n")
}

export function buildStoryUserMessage(input: { material: string; dna?: string; medium?: string }): string {
  const parts = ["【真实故事素材】" + input.material]
  if (input.dna) {
    parts.push("")
    parts.push("【传递卡 / DNA】")
    parts.push(input.dna)
    parts.push("（人设、发愿、立场、语调——这些决定故事的语气和关系。）")
  }
  if (input.medium && input.medium !== "auto") {
    parts.push("【介质偏好】" + input.medium)
  }
  parts.push("")
  parts.push("先判定框架（六选一：熵增与不可逆/非等价交换/镜像与和解/巨物与蜉蝣/凡人之旅/真诚的闭环），再按介质写故事。开头必须激活认知异常。介质决定写法——口播身体感，Vlog白描。故事命中造神模板+讨喜共识。结尾不给答案。只输出脚本正文。")
  return parts.join("\n")
}
