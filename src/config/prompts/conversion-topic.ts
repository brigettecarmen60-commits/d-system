export function buildConversionTopicUserMessage(input: { niche: string; dna?: string }): string {
  const parts = ["【赛道】" + input.niche]
  if (input.dna) {
    parts.push("")
    parts.push("【DNA / 定位信息】")
    parts.push(input.dna)
    parts.push("（读出镜人的货、卖点、人设——这些决定秀肌肉方式和卖点镜头选择。）")
  }
  parts.push("")
  parts.push("脑内完整执行：决策路径5节点锁定→人货场识别→卖点提炼(3步筛选+镜头翻译)→秀肌肉方式匹配。但只输出4条选题。每条含：标题+怎么拍+顾客卡点+卖点镜头+人性驱动+代码+为什么推得动+节点。选题比例：节点1+2共2条/节点3+4共1条/节点5共1条。覆盖自检在脑内跑完。")
  return parts.join("\n")
}
