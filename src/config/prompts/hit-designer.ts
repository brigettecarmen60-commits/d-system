export function buildHitDesignerUserMessage(input: {
  topic?: string
  niche: string
  material?: string
  dna?: string
}): string {
  const parts = ["【赛道】" + input.niche]
  if (input.topic) parts.push("【选题】" + input.topic)
  if (input.material) parts.push("【素材】" + input.material)
  if (input.dna) parts.push("【DNA】" + input.dna)
  parts.push("")
  parts.push("三条传播结构。")
  return parts.join("\n")
}
