export function buildTrafficCoreUserMessage(input: {
  niche: string
  targetAudience?: string
  targetGap?: string
  dna?: string
}): string {
  const parts = ["【赛道】" + input.niche]
  if (input.targetAudience) parts.push("【目标受众】" + input.targetAudience)
  if (input.targetGap) parts.push("【目标缺口】" + input.targetGap)
  if (input.dna) {
    parts.push("")
    parts.push("【DNA/定位】")
    parts.push(input.dna)
  }
  parts.push("")
  parts.push("脑内完整执行：感知文化水温→17母题扫描→13跨域扫描→冷热判断→列20+物理触点→9步出题(每条走完)→只输出6条。每条6行：标题+拍+停+站+代码+分享。停≤15字不含赛道词。覆盖自检脑内跑完。冷门赛道必须用大池子入口思维——选题入口是所有人都经历的事，不是你行业的事。")
  return parts.join("\n")
}
